package StageOne.day03;

public class norm {
    public static void main(String[] args) {
        int a=7,b=10;
        System.out.println(a%b);
        int c=5,d=5;
        c++;d--;
        System.out.println(c+"   "+d);
        c=5;d=5;
        System.out.println(c++);
        System.out.println(++d);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(c==d);
        System.out.println(c!=a);

    }
}
